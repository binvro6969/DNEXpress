    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String accountIdParam = request.getParameter("service_type_id");
        int ser_id = Integer.parseInt(accountIdParam);
        boolean propertiesDeleted = managerImplement.deleteService(ser_id);

        if (propertiesDeleted) {
            request.setAttribute("successMessage", "Service Type deleted successfully.");
        } else {
            request.setAttribute("errorMessage", "Failed to delete service type.");
        }

        response.sendRedirect("Manager_listService_Servlet");
    }

